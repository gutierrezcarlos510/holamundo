package Modelos;

public class Dato {
	private String log_dat;
	private String cla_dat;
	private int cod_per;
	public String getLog_dat() {
		return log_dat;
	}
	public void setLog_dat(String log_dat) {
		this.log_dat = log_dat;
	}
	public String getCla_dat() {
		return cla_dat;
	}
	public void setCla_dat(String cla_dat) {
		this.cla_dat = cla_dat;
	}
	public int getCod_per() {
		return cod_per;
	}
	public void setCod_per(int cod_per) {
		this.cod_per = cod_per;
	}
}
